def _stack_type_pytypes(st: StackType) -> Sequence[pytypes.PyType]:
    try:
        return STACK_TYPE_MAPPING[st]
    except KeyError as ex:
        raise NotImplementedError(f"Could not map stack type {st} to an algopy type") from ex


def _result_pytype(st: StackType) -> pytypes.RuntimeType:
    assert st is not StackType.any
    pt = _stack_type_pytypes(st)[0]
    assert isinstance(pt, pytypes.RuntimeType)
    return pt


def _stack_python_type(st: StackType) -> str:
    """Python type annotation (union) for a stack type in a stub signature."""
    allowed = _stack_type_pytypes(st)
    # Hide types that are subtypes of another type in the list
    # (e.g. Bytes is redundant when BytesBacked is present, because Bytes is a BytesBacked).
    displayed = [t for t in allowed if not any(other < t for other in allowed if other != t)]
    names = [str(pt).removeprefix("algopy.") for pt in displayed]
    for pt in displayed:
        lit = PYTYPE_TO_LITERAL[pt]
        if lit is not None and (lit_name := str(lit)) not in names:
            names.append(lit_name)
    return " | ".join(names)


def _immediate_kind_pytype(kind: ImmediateKind) -> type[int | str]:
    match kind:
        case ImmediateKind.uint8 | ImmediateKind.int8 | ImmediateKind.varuint:
            return int
        case ImmediateKind.arg_enum:
            return str
        case _:
            raise ValueError(f"Unexpected ImmediateKind: {kind}")


def _enum_class(arg_enum: str) -> str:
    if (override := PYTHON_ENUM_CLASS.get(arg_enum)) is not None:
        return override
    # don't change acronyms
    if arg_enum.isupper():
        return arg_enum
    return _snake_case(arg_enum).replace("_", " ").title().replace(" ", "")


# HACK: these variants are hidden from docstrings — optimiser-picked alternates that are
# noise to users. Covers embedded-immediate forms (e.g. arg_0) via the regex, plus explicit
# names that don't match a pattern.
_DOC_HIDDEN_VARIANT_RE = re.compile(r"_\d+$")
_DOC_HIDDEN_VARIANTS = {"load", "store", "replace"}


def _op_codes_for_doc_links(op_name: str) -> list[str]:
    variants = [
        v
        for v in OP_VARIANTS.get(op_name, ())
        if v not in _DOC_HIDDEN_VARIANTS and not _DOC_HIDDEN_VARIANT_RE.search(v)
    ]
    return sorted({op_name, *variants})


def _algorand_doc(op: str) -> str:
    return f"[`{op}`]({ALGORAND_OP_URL}#{op})"


def _op_doc(op: Op) -> list[str]:
    doc = [d.replace("\\", "\\\\") for d in op.doc]
    return doc + _min_version_note(op.min_avm_version)


def _min_version_note(version: int) -> list[str]:
    # only output min AVM version if it exceeds the base supported version
    return [f"Min AVM version: {version}"] if version > MIN_SUPPORTED_VERSION else []


def _snake_case(s: str) -> str:
    s = s.replace("-", " ")
    s = re.sub(r"([A-Z]+)([A-Z][a-z])", r"\1_\2", s)
    s = re.sub(r"([a-z\d])([A-Z])", r"\1_\2", s)
    return re.sub(r"[-\s]", "_", s).lower()


def _pytype_stub_repr(pt: pytypes.PyType) -> str:
    return str(pt).replace("algopy.", "")


