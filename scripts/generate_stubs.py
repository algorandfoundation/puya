@attrs.frozen
class StubArg:
    name: str
    python_type: str  # pre-rendered stub type, e.g. "UInt64 | int"
    doc: str | None


@attrs.frozen(kw_only=True)
class Method:
    name: str  # Python function/method name
    op_code: str  # TEAL opcode referenced by the method
    doc: list[str]
    return_docs: list[str]
    min_avm_version: int
    stub_args: list[StubArg]
    # None => halts; [] => returns None; else result stack types
    result_types: list[pytypes.RuntimeType] | None
    # awst mapping: positional immediate literals, or expected immediate type
    immediates: list[str | type[str | int]]
    # awst mapping: per-call-arg expected pytypes, or int index into `immediates`
    arg_mappings: list[tuple[pytypes.PyType, ...] | int]
    is_property: bool

    @property
    def result_pytype(self) -> pytypes.PyType:
        return result_types_to_pytype(self.result_types)


@attrs.frozen(kw_only=True)
class ClassSpec:
    name: str
    doc: str
    ops: list[str]  # sorted TEAL opcodes covered by this class (for docstring)
    methods: list[Method]


