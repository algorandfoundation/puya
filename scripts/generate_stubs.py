def _methods_for_op(op: Op, py_name: str) -> Iterator[Method]:
    """Build methods for ``op``; forks into bytes/uint64 variants if an output is `any`."""
    if any(so.stack_type is StackType.any for so in op.stack_outputs):
        logger.info(f"Found any output for {op.name}")
        for replacement in (StackType.bytes, StackType.uint64):
            yield _build_method(_replace_any(op, replacement), f"{py_name}_{replacement.name}")
    else:
        yield _build_method(op, py_name)
    logger.info(f"Mapping {op.name} to {py_name}")


def _methods_for_overriding_immediate(
    spec: LanguageSpec, op: Op, immediate: Immediate, *, method_name_prefix: str = ""
) -> Iterator[Method]:
    """Build one method per value of an arg-enum immediate that swaps stack types."""
    assert immediate.arg_enum
    logger.info(f"Using overriding immediate for {op.name}")
    for value in spec.arg_enums[immediate.arg_enum]:
        assert value.stack_type
        # The immediate value swaps exactly one stack input or output's type.
        if immediate.modifies_stack_input is not None:
            inputs = list(op.stack_inputs)
            idx = immediate.modifies_stack_input
            inputs[idx] = attrs.evolve(inputs[idx], stack_type=value.stack_type, doc=value.doc)
            per_value_op = attrs.evolve(op, stack_inputs=inputs)
        else:
            assert immediate.modifies_stack_output is not None
            outputs = list(op.stack_outputs)
            idx = immediate.modifies_stack_output
            outputs[idx] = attrs.evolve(outputs[idx], stack_type=value.stack_type, doc=value.doc)
            per_value_op = attrs.evolve(op, stack_outputs=outputs)

        method_name = _snake_case(value.name)
        if method_name_prefix:
            method_name = f"{method_name_prefix}_{method_name}"
        # Enums may be reused across ops; take the max minimum version of op and enum value.
        method_version = max(op.min_avm_version, value.min_avm_version)
        method = _build_method(per_value_op, method_name, pinned_immediate=(immediate, value))
        yield attrs.evolve(
            method,
            min_avm_version=method_version,
            doc=method.doc + _min_version_note(method_version),
        )


def _build_method(
    op: Op, py_name: str, *, pinned_immediate: tuple[Immediate, ArgEnum] | None = None
) -> Method:
    stub_args = list[StubArg]()
    immediates = list[str | type[str | int]]()
    arg_mappings = list[tuple[pytypes.PyType, ...] | int]()

    for imm_idx, imm in enumerate(op.immediate_args):
        if pinned_immediate and pinned_immediate[0] == imm:
            # pinned: omit from stub signature, inline the enum value in the awst mapping
            immediates.append(pinned_immediate[1].name)
            continue
        if imm.immediate_type == ImmediateKind.arg_enum:
            assert imm.arg_enum, "Arg enum expected"
            stub_type = _enum_class(imm.arg_enum)
        else:
            stub_type = _immediate_kind_pytype(imm.immediate_type).__name__
        stub_args.append(StubArg(name=imm.name.lower(), python_type=stub_type, doc=imm.doc))
        immediates.append(_immediate_kind_pytype(imm.immediate_type))
        arg_mappings.append(imm_idx)

    for si in op.stack_inputs:
        stub_args.append(
            StubArg(
                name=si.name.lower(), python_type=_stack_python_type(si.stack_type), doc=si.doc
            )
        )
        arg_mappings.append(tuple(_stack_type_pytypes(si.stack_type)))

    doc = [] if pinned_immediate else _op_doc(op)

    if op.halts:
        return_docs = ["Halts program"]
        result_types = None
    else:
        return_docs = [so.doc for so in op.stack_outputs if so.doc]
        if not op.stack_outputs:
            result_types = list[pytypes.RuntimeType]()
        elif py_name == "on_completion":
            result_types = [pytypes.OnCompleteActionType]
        elif py_name == "type_enum":
            result_types = [pytypes.TransactionTypeType]
        else:
            result_types = [_result_pytype(o.stack_type) for o in op.stack_outputs]

    is_property = op.name in PROPERTY_OPS and py_name not in PROPERTY_OPS[op.name]

    return Method(
        name=py_name,
        op_code=op.name,
        doc=doc,
        return_docs=return_docs,
        min_avm_version=op.min_avm_version,
        stub_args=stub_args,
        result_types=result_types,
        immediates=immediates,
        arg_mappings=arg_mappings,
        is_property=is_property,
    )


def _build_merged_class(spec: LanguageSpec, group: MergedOpCodes) -> ClassSpec:
    # later op wins on name collision; dict preserves first-insertion order
    methods = dict[str, Method]()
    for op_name in group.ops:
        op = spec.ops[op_name]
        overriding = _overriding_immediate(op)
        assert overriding
        for method in _methods_for_overriding_immediate(spec, op, overriding):
            methods[method.name] = method
    doc = group.doc
    if doc is None:
        doc = " ".join(line for op_name in group.ops for line in spec.ops[op_name].doc)
    return ClassSpec(
        name=group.name, doc=doc, ops=sorted(group.ops), methods=list(methods.values())
    )


def _build_grouped_class(
    spec: LanguageSpec, group: GroupedOpCodes, enums: dict[str, None]
) -> ClassSpec:
    methods = list[Method]()
    for op_name, py_name in group.ops.items():
        op = spec.ops[op_name]
        overriding = _overriding_immediate(op)
        if overriding:
            methods.extend(
                _methods_for_overriding_immediate(spec, op, overriding, method_name_prefix=py_name)
            )
        else:
            methods.extend(_methods_for_op(op, py_name))
        _collect_stack_arg_enums(op, enums)
    return ClassSpec(name=group.name, doc=group.doc, ops=sorted(group.ops), methods=methods)


def _replace_any(op: Op, replace: StackType) -> Op:
    return attrs.evolve(
        op,
        stack_inputs=[
            attrs.evolve(si, stack_type=replace) if si.stack_type is StackType.any else si
            for si in op.stack_inputs
        ],
        stack_outputs=[
            attrs.evolve(so, stack_type=replace) if so.stack_type is StackType.any else so
            for so in op.stack_outputs
        ],
    )


def _collect_stack_arg_enums(op: Op, enums: dict[str, None]) -> None:
    for arg in op.immediate_args:
        if (
            arg.immediate_type == ImmediateKind.arg_enum
            and arg.modifies_stack_input is None
            and arg.modifies_stack_output is None
        ):
            assert arg.arg_enum is not None
            enums[arg.arg_enum] = None


def _overriding_immediate(op: Op) -> Immediate | None:
    return next(
        (
            i
            for i in op.immediate_args
            if i.modifies_stack_input is not None or i.modifies_stack_output is not None
        ),
        None,
    )


