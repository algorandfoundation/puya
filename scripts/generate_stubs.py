def main() -> None:
    spec_path = VCS_ROOT / "langspec.puya.json"
    spec_text = spec_path.read_text(encoding="utf-8")
    spec_json = json.loads(spec_text)
    spec = LanguageSpec.from_json(spec_json)

    handled = EXCLUDED_OPCODES.union(*OP_VARIANTS.values())
    top_level = list[Method]()
    classes = list[ClassSpec]()
    enums = dict[str, None]()

    for merged in MERGED_OP_CODES:
        handled.update(merged.ops)
        classes.append(_build_merged_class(spec, merged))
    for grouped in GROUPED_OP_CODES:
        handled.update(grouped.ops)
        classes.append(_build_grouped_class(spec, grouped, enums))
    for op in spec.ops.values():
        if op.name in handled or not op.name.isidentifier():
            logger.info(f"Ignoring: {op.name}")
        else:
            # derive Python name: explicit rename, else shortest name in OP_VARIANTS family
            # (e.g. TEAL `args` becomes Python `arg()`), else the op name itself.
            if op_variants := OP_VARIANTS.get(op.name):
                py_name = min(op.name, *op_variants, key=len)
            else:
                py_name = RENAMED_OP_CODES.get(op.name, op.name)
            _collect_stack_arg_enums(op, enums)
            top_level.extend(_methods_for_op(op, py_name))

    top_level.sort(key=lambda m: m.name)
    classes.sort(key=lambda c: c.name)
    enum_names = list(enums)

    _write_stub(spec, enum_names, top_level, classes)
    _write_awst_data(spec, enum_names, top_level, classes)


