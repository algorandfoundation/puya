def _render_enum_stub(spec: LanguageSpec, arg_enum: str) -> str:
    enum_name = _enum_class(arg_enum)
    lines = [
        f"class {enum_name}(str):",
        f'{INDENT}"""Available values for the `{arg_enum}` enum"""',
    ]
    for value in spec.arg_enums[arg_enum]:
        lines.append(f"{INDENT}{value.name}: {enum_name} = ...")
        value_doc = ([value.doc] if value.doc else []) + _min_version_note(value.min_avm_version)
        if value_doc:
            lines.append(f'{INDENT}"""')
            lines.extend(f"{INDENT}{line}" for line in value_doc)
            lines.append(f'{INDENT}"""')
    lines.append("")
    return "\n".join(lines)


def _render_method_stub(method: Method, prefix: str = "") -> str:
    returns = _pytype_stub_repr(method.result_pytype)
    args = [f"{a.name}: {a.python_type}" for a in method.stub_args]
    if args:
        args.append("/")  # TODO: remove once we support kwargs
    signature = f"{prefix}def {method.name}({', '.join(args)}) -> {returns}:"
    doc = list(method.doc)
    doc.extend(f":param {a.python_type} {a.name}: {a.doc}" for a in method.stub_args if a.doc)
    if method.return_docs:
        if doc:
            doc.append(f":returns {returns}: {method.return_docs[0]}")
            doc.extend(method.return_docs[1:])
        else:
            doc = list(method.return_docs)
    teal_refs = ", ".join(_algorand_doc(op) for op in _op_codes_for_doc_links(method.op_code))
    doc.extend(("", f"Native TEAL opcode: {teal_refs}"))
    body_lines = ['"""', *doc, '"""'] if doc else ["..."]
    body_indent = prefix + INDENT
    body = "\n".join(body_indent + line for line in body_lines)
    return f"{signature}\n{body}"


def _render_class_var_stub(method: Method, indent: str) -> str:
    returns = _pytype_stub_repr(method.result_pytype)
    doc = method.return_docs or method.doc
    doc = list(doc) + _min_version_note(method.min_avm_version)
    lines = [
        f"{indent}{method.name}: typing.Final[{returns}] = ...",
        f'{indent}"""',
        *(f"{indent}{line}" for line in doc),
        f'{indent}"""',
    ]
    return "\n".join(lines)


def _render_class_stub(klass: ClassSpec) -> str:
    op_refs = [_algorand_doc(op) for op in klass.ops]
    lines = [
        f"class {klass.name}:",
        INDENT + '"""',
        INDENT + klass.doc,
        INDENT + f"Native TEAL op{'s' if len(op_refs) > 1 else ''}: {', '.join(op_refs)}",
        INDENT + '"""',
    ]
    for method in klass.methods:
        if method.is_property:
            lines.append(_render_class_var_stub(method, INDENT))
        else:
            lines.append(f"{INDENT}@staticmethod")
            lines.append(_render_method_stub(method, prefix=INDENT))
        lines.append("")
    return "\n".join(lines)


def _write_stub(
    spec: LanguageSpec, enums: list[str], top_level: list[Method], classes: list[ClassSpec]
) -> None:
    references = ", ".join(
        sorted(
            str(pt).removeprefix("algopy.")
            for pt in PYTYPE_TO_LITERAL
            if str(pt).startswith("algopy.")
        )
    )
    out = [
        autogenerated_header_for(__file__),
        "import typing",
        "",
        f"from algopy import {references}",
    ]
    out.extend(_render_enum_stub(spec, e) for e in enums)
    for method in top_level:
        if method.name == "exit":
            out.append(_ERR_FUNCTION_STUB)
        out.append(_render_method_stub(method))
    out.extend(_render_class_stub(c) for c in classes)

    path = VCS_ROOT / "stubs" / "algopy-stubs" / "op.pyi"
    path.write_text("\n".join(out), encoding="utf-8")
    subprocess.run(["ruff", "format", str(path)], check=True, cwd=VCS_ROOT)


