def _pytype_repr(typ: pytypes.PyType) -> str:
    try:
        return PYTYPE_REPR[typ]
    except KeyError:
        pass
    match typ:
        case pytypes.TupleType(items=items) if len(items) > 1:
            inner = ", ".join(_pytype_repr(t) for t in items)
            return f"pytypes.GenericTupleType.parameterise(({inner}), source_location=None)"
    raise ValueError(f"Unexpected pytype: {typ}")


def _immediate_repr(item: str | int | type[str | int]) -> str:
    return item.__name__ if isinstance(item, type) else repr(item)


def _arg_repr(arg: Sequence[pytypes.PyType] | int) -> str:
    if isinstance(arg, int):
        return repr(arg)
    inner = ", ".join(_pytype_repr(t) for t in arg)
    return f"({inner},)" if len(arg) == 1 else f"({inner})"


def _render_op_mapping(method: Method) -> str:
    if method.is_property:
        (immediate,) = method.immediates
        return (
            f"{method.name}=PropertyOpMapping("
            f"{method.op_code!r}, {immediate!r}, {_pytype_repr(method.result_pytype)},"
            f"),"
        )
    parts = [f"op_code={method.op_code!r}"]
    if method.result_types is None:
        parts.append("result=None")
    elif method.result_types:
        parts.append(f"result=[{', '.join(_pytype_repr(r) for r in method.result_types)}]")
    if method.immediates:
        parts.append(f"immediates=[{', '.join(_immediate_repr(i) for i in method.immediates)}]")
    if method.arg_mappings:
        parts.append(f"args=[{', '.join(_arg_repr(a) for a in method.arg_mappings)}]")
    return f"{method.name}=FunctionOpMapping({', '.join(parts)},),"


def _build_awst_data(
    spec: LanguageSpec, enums: list[str], top_level: list[Method], classes: list[ClassSpec]
) -> str:
    lines = [
        autogenerated_header_for(__file__),
        "import typing",
        "from collections.abc import Mapping, Sequence",
        "from puyapy.awst_build import pytypes",
        "from puyapy.awst_build.intrinsic_models import FunctionOpMapping, PropertyOpMapping",
        "ENUM_CLASSES: typing.Final[Mapping[str, Mapping[str, str]]] = dict(",
    ]
    for enum_name in enums:
        lines.append(f"{_enum_class(enum_name)}=dict(")
        # enum names currently match enum immediate values
        lines.extend(f'{v.name}="{v.name}",' for v in spec.arg_enums[enum_name])
        lines.append("),")
    lines.append(")")
    lines.append("")
    lines.append("FUNC_TO_AST_MAPPER: typing.Final[Mapping[str, FunctionOpMapping]] = dict(")
    lines.extend(_render_op_mapping(m) for m in top_level)
    lines.append(")")
    lines.append(
        "NAMESPACE_CLASSES: "
        "typing.Final[Mapping[str, Mapping[str, PropertyOpMapping | FunctionOpMapping]]]"
        " = dict("
    )
    for klass in classes:
        lines.append(f"{klass.name}=dict(")
        lines.extend(_render_op_mapping(m) for m in klass.methods)
        lines.append("),")
    lines.append(")")
    return "\n".join(lines)


def _write_awst_data(
    spec: LanguageSpec, enums: list[str], top_level: list[Method], classes: list[ClassSpec]
) -> None:
    path = VCS_ROOT / "src" / "puyapy" / "awst_build" / "intrinsic_data.py"
    path.write_text(_build_awst_data(spec, enums, top_level, classes), encoding="utf-8")
    subprocess.run(["ruff", "format", str(path)], check=True, cwd=VCS_ROOT)
    subprocess.run(["ruff", "check", "--fix", str(path)], check=False, cwd=VCS_ROOT)


